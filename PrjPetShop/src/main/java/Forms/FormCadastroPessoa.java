    private void preencherCombo() {
        Pessoa pessoaCons = new Pessoa();
        PessoaBO pBO = new PessoaBO();
        
        String nome = txtConsNome.getText();
        if(!nome.isEmpty()) {
            pessoaCons.setNome(nome);
            lstPessoas = pBO.getPessoas(pessoaCons);
            cmbPessoas.removeAllItems();
            lstPessoas.forEach(itemPessoa -> {
                cmbPessoas.addItem(itemPessoa.getNome() + " | " + itemPessoa.getIdpessoa());
            });
        } else {
            cmbPessoas.removeAllItems();
        }
    }
        
    public void preencherCampos() {
        if(!lstPessoas.isEmpty()) {
            int index = cmbPessoas.getSelectedIndex();
            Pessoa pessoa = lstPessoas.get(index);
            
            txtCodigo.setText(String.valueOf(pessoa.getIdpessoa()));
            txtNome.setText(pessoa.getNome());
            txtDataNasc.setText(pessoa.getData());
            btnSalvar.setEnabled(false);
        }
    }
        
    public void novo() {
            txtConsNome.setText("");
            lstPessoas = new ArrayList<>();
            txtCodigo.setText("");
            txtNome.setText("");
            txtDataNasc.setText("");
            btnSalvar.setEnabled(true);
    }
    
